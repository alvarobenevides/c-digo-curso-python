'''
Python = Linguagem de progamação
Tipo de tipagem = Dinâmica \ Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''
print(123)
print(1, 'Álvaro "Luis"')

# As aspas simples
print('Álvaro Luis')
print(1, 'Álvaro "Luis"')

# Aspas duplas
print("Álvaro Luis")
print(2, "Álvaro 'Luis'")

# Escape
print("Álvaro \"Luis\"")

# r
print(r"Álvaro \"Luis\"")
